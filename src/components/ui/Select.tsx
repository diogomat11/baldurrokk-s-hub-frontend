// Componente Select com search
interface SelectWithSearchProps {
  value: string
  onValueChange: (value: string) => void
  placeholder?: string
  options: { label: string; value: string; disabled?: boolean }[]
  label?: string
  error?: string
  required?: boolean
  className?: string
  disabled?: boolean
}

export const SelectWithSearch: React.FC<SelectWithSearchProps> = ({
  value,
  onValueChange,
  placeholder = "Selecione uma opção",
  options,
  label,
  error,
  required,
  className,
  disabled = false,
}) => {
  const [searchTerm, setSearchTerm] = React.useState("")
  const [isOpen, setIsOpen] = React.useState(false)

  const filteredOptions = React.useMemo(() => {
    return options.filter(option =>
      option.label.toLowerCase().includes(searchTerm.toLowerCase())
    )
  }, [options, searchTerm])

  const selectedOption = options.find(option => option.value === value)

  return (
    <Select 
      value={value} 
      onValueChange={onValueChange}
      disabled={disabled}
    >
      <SelectTrigger
        error={!!error}
        label={label}
        required={required}
        className={className}
      >
        <SelectValue placeholder={placeholder}>
          {selectedOption?.label}
        </SelectValue>
      </SelectTrigger>
      <SelectContent>
        <div className="p-2">
          <input
            type="text"
            placeholder="Buscar..."
            value={searchTerm}
            onChange={(e) => setSearchTerm(e.target.value)}
            className="w-full px-3 py-2 text-sm border border-input rounded-xl focus:outline-none focus:ring-2 focus:ring-ring"
            autoFocus
          />
        </div>
        <SelectPrimitive.Separator />
        {filteredOptions.length === 0 ? (
          <div className="p-2 text-sm text-muted-foreground text-center">
            Nenhuma opção encontrada
          </div>
        ) : (
          filteredOptions.map((option) => (
            <SelectItem
              key={option.value}
              value={option.value}
              disabled={option.disabled}
            >
              {option.label}
            </SelectItem>
          ))
        )}
      </SelectContent>
    </Select>
  )
}